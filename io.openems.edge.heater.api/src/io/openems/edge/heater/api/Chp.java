package io.openems.edge.heater.api;

import io.openems.common.channel.AccessMode;
import io.openems.common.channel.Unit;
import io.openems.common.exceptions.OpenemsError.OpenemsNamedException;
import io.openems.common.types.OpenemsType;
import io.openems.edge.common.channel.BooleanReadChannel;
import io.openems.edge.common.channel.BooleanWriteChannel;
import io.openems.edge.common.channel.Doc;
import io.openems.edge.common.channel.DoubleReadChannel;
import io.openems.edge.common.channel.DoubleWriteChannel;
import io.openems.edge.common.channel.IntegerReadChannel;
import io.openems.edge.common.channel.IntegerWriteChannel;
import io.openems.edge.common.channel.value.Value;
import io.openems.edge.common.component.OpenemsComponent;

/**
 * A generalized interface for a combined heat and power generator (chp).
 * Contains the most important functions that should be available on all chp, allowing a vendor agnostic 
 * implementation. Vendor specific interfaces should extend this interface.
 *
 */

public interface Chp extends Heater {

    enum ChannelId implements io.openems.edge.common.channel.ChannelId {

        /**
         * Electric power generated by the chp at the moment.
		 *
		 * <ul>
		 *     <li> Interface: Chp
		 *     <li> Type: Double
		 *     <li> Unit: kilowatt
		 * </ul>
         */
        EFFECTIVE_ELECTRIC_POWER(Doc.of(OpenemsType.DOUBLE).unit(Unit.KILOWATT).accessMode(AccessMode.READ_ONLY)),

		/**
		 * Set point for the generated electrical power.
		 *
		 * <ul>
		 *     <li> Interface: Chp
		 *     <li> Type: Integer
		 *     <li> Unit: kilowatt
		 * </ul>
		 */
		EFFECTIVE_ELECTRIC_POWER_SETPOINT(Doc.of(OpenemsType.DOUBLE).unit(Unit.KILOWATT).accessMode(AccessMode.READ_WRITE));
    	
        private final Doc doc;

        private ChannelId(Doc doc) {
            this.doc = doc;
        }

        public Doc doc() {
            return this.doc;
        }

    }

    /**
     * Gets the Channel for {@link ChannelId#EFFECTIVE_ELECTRIC_POWER}.
     *
     * @return the Channel
     */
    public default DoubleReadChannel getEffectiveElectricPowerChannel() {
        return this.channel(ChannelId.EFFECTIVE_ELECTRIC_POWER);
    }
    
    /**
     * Get the currently generated electric power of the chp in kilowatt. Value contains a double.
	 * See {@link ChannelId#EFFECTIVE_ELECTRIC_POWER}.
	 *
	 * @return the Channel {@link Value}
     */
    public default Value<Double> getEffectiveElectricPower() {
		return this.getEffectiveElectricPowerChannel().value();
	}

    /**
	 * Internal method to set the 'nextValue' on {@link ChannelId#EFFECTIVE_ELECTRIC_POWER}
	 * Channel.
	 *
	 * @param value the next value
	 */
    public default void _setEffectiveElectricPower(Double value) {
		this.getEffectiveElectricPowerChannel().setNextValue(value);
	}
	
	/**
	 * Internal method to set the 'nextValue' on {@link ChannelId#EFFECTIVE_ELECTRIC_POWER}
	 * Channel.
	 *
	 * @param value the next value
	 */
	public default void _setEffectiveElectricPower(double value) {
		this.getEffectiveElectricPowerChannel().setNextValue(value);
	}

	/**
	 * Gets the Channel for {@link ChannelId#EFFECTIVE_ELECTRIC_POWER_SETPOINT}.
	 *
	 * @return the Channel
	 */
	public default DoubleWriteChannel getElectricPowerSetpointChannel() {
		return this.channel(ChannelId.EFFECTIVE_ELECTRIC_POWER_SETPOINT);
	}

	/**
	 * Electric power set point. See {@link ChannelId#EFFECTIVE_ELECTRIC_POWER_SETPOINT}.
	 *
	 * @return the Channel {@link Value}
	 */
	public default Value<Double> getElectricPowerSetpoint() {
		return this.getElectricPowerSetpointChannel().value();
	}

	/**
	 * Internal method to set the 'nextValue' on {@link ChannelId#EFFECTIVE_ELECTRIC_POWER_SETPOINT}
	 * Channel.
	 *
	 * @param value the next value
	 */
	public default void _setElectricPowerSetpoint(Double value) {
		this.getElectricPowerSetpointChannel().setNextValue(value);
	}

	/**
	 * Internal method to set the 'nextValue' on {@link ChannelId#EFFECTIVE_ELECTRIC_POWER_SETPOINT}
	 * Channel.
	 *
	 * @param value the next value
	 */
	public default void _setElectricPowerSetpoint(double value) {
		this.getElectricPowerSetpointChannel().setNextValue(value);
	}

	/**
	 * Set electric power set point. See {@link ChannelId#EFFECTIVE_ELECTRIC_POWER_SETPOINT}.
	 *
	 * @param value the next write value
	 * @throws OpenemsNamedException on error
	 */
	public default void setElectricPowerSetpoint(Double value) throws OpenemsNamedException {
		this.getElectricPowerSetpointChannel().setNextWriteValue(value);
	}

	/**
	 * Set electric power set point. See {@link ChannelId#EFFECTIVE_ELECTRIC_POWER_SETPOINT}.
	 *
	 * @param value the next write value
	 * @throws OpenemsNamedException on error
	 */
	public default void setElectricPowerSetpoint(double value) throws OpenemsNamedException {
		this.getElectricPowerSetpointChannel().setNextWriteValue(value);
	}
}
